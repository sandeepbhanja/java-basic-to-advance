public class assignmentOperators{
    public static void main(String args[]){
        int n1 = 7;
        int n2 = 8;
        int result1 = n1+n2;
        int result2 = n1-n2;
        int result3 = n1*n2;
        int result4 = n1/n2;
        int result5 = n1%n2;
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);
        System.out.println(result5);
    }
}